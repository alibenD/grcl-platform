"""Explicit `grcl_result_t` mappings for the Python wrapper skeleton."""

from __future__ import annotations

from enum import IntEnum

GRCL_OK = 0x0000
GRCL_ERROR_INVALID_ARGUMENT = 0x0101
GRCL_ERROR_BAD_STATE = 0x0102
GRCL_ERROR_OUT_OF_MEMORY = 0x0201
GRCL_ERROR_CAPACITY_EXCEEDED = 0x0202
GRCL_ERROR_UNSUPPORTED_CAPABILITY = 0x0301
GRCL_ERROR_INCOMPATIBLE_VERSION = 0x0302
GRCL_ERROR_TIMEOUT = 0x0401
GRCL_ERROR_INTERRUPTED = 0x0402
GRCL_ERROR_BACKEND_FAILURE = 0x0403
GRCL_ERROR_PEER_UNAVAILABLE = 0x0501
GRCL_ERROR_CHANNEL_REJECTED = 0x0502
GRCL_ERROR_UNKNOWN = 0x7FFF


class Result(IntEnum):
    """Mirror of the accepted `grcl_result_t` values."""

    OK = GRCL_OK
    ERROR_INVALID_ARGUMENT = GRCL_ERROR_INVALID_ARGUMENT
    ERROR_BAD_STATE = GRCL_ERROR_BAD_STATE
    ERROR_OUT_OF_MEMORY = GRCL_ERROR_OUT_OF_MEMORY
    ERROR_CAPACITY_EXCEEDED = GRCL_ERROR_CAPACITY_EXCEEDED
    ERROR_UNSUPPORTED_CAPABILITY = GRCL_ERROR_UNSUPPORTED_CAPABILITY
    ERROR_INCOMPATIBLE_VERSION = GRCL_ERROR_INCOMPATIBLE_VERSION
    ERROR_TIMEOUT = GRCL_ERROR_TIMEOUT
    ERROR_INTERRUPTED = GRCL_ERROR_INTERRUPTED
    ERROR_BACKEND_FAILURE = GRCL_ERROR_BACKEND_FAILURE
    ERROR_PEER_UNAVAILABLE = GRCL_ERROR_PEER_UNAVAILABLE
    ERROR_CHANNEL_REJECTED = GRCL_ERROR_CHANNEL_REJECTED
    ERROR_UNKNOWN = GRCL_ERROR_UNKNOWN


def from_native_result(value: int | Result) -> Result:
    """Convert a native `grcl_result_t` value into the local enum."""

    return value if isinstance(value, Result) else Result(value)


